a=int(input("Unesite prvi ceo broj: "))   
b=int(input("Unesite drugi ceo broj: ")) 

print("Vrednost izraza {}>{} je {}".format(a,b,a>b))
print("Vrednost izraza {}<{} je {}".format(a,b,a<b))
print("Vrednost izraza {}=={} je {}".format(a,b,a==b))
print("Vrednost izraza {}!={} je {}".format(a,b,a!=b))
