#prvi način koristeći for petlju. Postavljamo treci argument da bude 2 tako da se brojac i povecava za 2 i ima samo parne vrednosti
for i in range(0,101,2):
    print(i)

#prvi način koristeći while petlju
i=0
while i<101:
    print(i)
    #Povecavamo brojac i za 2 kako bismo ispisivali samo parne vrednosti
    i=i+2
